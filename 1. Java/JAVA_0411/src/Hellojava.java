//실행 단축키 : ctrl + f11
public class Hellojava {
	// 프로그램 실행을 시킬 때, "메인함수"에 있는 내용을 위주로 실행함
	public static void main(String[] args) { //메인함수
		// 여기에 주로 코드를 작성하면 됨
		// ;(세미콜론) :: "문장을 끝낸다."
		
		// 주석 :: 코드에 대한 설명이나 소개를 작성하기 위한 문법
		// 주석 처리 단축키 : ctrl + /
		
		// 출력문 :: 결과값을 보여주는 구문 
		// "print문 사용"이라는 문장을 출력하라.
		// ln을 붙이면 출력을 완료한 뒤 다음줄로 자동으로 넘어감
		System.out.println("println문 사용");
		// "\n" :: 다음줄로 넘어가서 출력을 계속 한다.
		System.out.print("print문 사용\n");
		System.out.print("print문 사용");		
		
		
		// 출력문에서 + 역할 :: 출력값을 "이어붙이는" 역할
	}

}